    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
        if(jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextArea1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please Fill the text fields");
        }
        else
        {
        String fname=jTextField1.getText();
        String lname=jTextField2.getText();
        String email=jTextField3.getText();
        String comment=jTextArea1.getText();
        String gender=(String) jComboBox1.getSelectedItem();

        
        if(new joint().checkEmail(email))
        {
            if(comment.length()<50 || comment.length()>100)
            {
                JOptionPane.showMessageDialog(null, "Comment should be between 50-100 Characters!");
            }
            else
            {
               String[] tbdata={fname,lname,email,comment,gender};
               DefaultTableModel tblModel=(DefaultTableModel)jTable1.getModel();
               tblModel.addRow(tbdata);
               jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextArea1.setText("");
                jButton1.setEnabled(false);  
               jButton2.setEnabled(true);  
            } 
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Invalid email!");
        }
    }
              
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         DefaultTableModel tblModel=(DefaultTableModel)jTable1.getModel();
        int selectedRowsCount= jTable1.getSelectedRowCount();
        int index=jTable1.getSelectedRow();
        if(selectedRowsCount==0)
        {
            JOptionPane.showMessageDialog(null, "Please Select User!");
        } 
        else if(selectedRowsCount>1)
        {
        JOptionPane.showMessageDialog(null, "Please Select a single User!","error",JOptionPane.ERROR_MESSAGE);}
        else{
        String fname=tblModel.getValueAt(index,0).toString();
        String lname=tblModel.getValueAt(index,1).toString();
        String email=tblModel.getValueAt(index,2).toString();
        String comment=tblModel.getValueAt(index,3).toString();
        String gender=tblModel.getValueAt(index,4).toString();
        new displayResult(fname,lname,email,comment,gender).setVisible(true);
        setVisible(false);
        }   
          
    }//GEN-LAST:event_jButton2ActionPerformed

